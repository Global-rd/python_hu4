# real life example: épitünk egy ikerházat egy adott telken

# program: a ház épitési terve, nem használ semmilyen erőforrást amig nem kezdődik az épitkezés
# process: a ház tényleges épitési folyamata, már erőforrásokat használva, ikerház miatt lehet a két oldal 2 process
# thread: falazás, festés, elektronika, gépszet stb. egy process-en belül, vagyis egy ház oldalon belül